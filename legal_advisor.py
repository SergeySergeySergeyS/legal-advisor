def get_system_prompt(category: str) -> str:
    """Возвращает системный промт для выбранной категории"""
    return CATEGORY_PROMPTS.get(category, GENERAL_PROMPT)


def safe_decode(content):
    """Безопасно декодирует байты в строку"""
    if isinstance(content, bytes):
        return content.decode('utf-8', errors='ignore')
    return content


def ask_legal_advisor(question: str, category: str, chat_history: list, auth_key: str) -> str:
    """
    Отправляет вопрос в GigaChat и возвращает ответ.
    Использует простой способ — передаём промт как строку.
    """
    try:
        # Создаём объект GigaChat
        llm = GigaChat(
            credentials=auth_key,
            scope="GIGACHAT_API_PERS",
            verify_ssl_certs=False
        )
        
        # Получаем системный промт для категории
        system_prompt = get_system_prompt(category)
        
        # Формируем полный промт как ОДНУ СТРОКУ
        full_prompt = system_prompt + "\n\n"
        
        # Добавляем историю чата (если есть)
        if chat_history:
            full_prompt += "ИСТОРИЯ ДИАЛОГА:\n"
            for msg in chat_history[-6:]:
                if msg["role"] == "user":
                    full_prompt += f"Вопрос: {msg['content']}\n"
                else:
                    full_prompt += f"Ответ: {msg['content']}\n"
            full_prompt += "\n"
        
        # Добавляем текущий вопрос
        full_prompt += f"НОВЫЙ ВОПРОС: {question}\n\n"
        full_prompt += "Дай структурированный ответ по шаблону."
        
        # === САМЫЙ ПРОСТОЙ СПОСОБ ВЫЗОВА ===
        # Вызываем llm как функцию с одной строкой
        response = llm(full_prompt)
        
        # Получаем ответ
        answer = response.choices[0].message.content
        answer = safe_decode(answer)
        
        return answer
        
    except Exception as e:
        return f"❌ Ошибка при обращении к ИИ: {str(e)}\n\nПопробуйте переформулировать вопрос или обратитесь позже."


def format_answer_for_display(answer: str) -> str:
    """Форматирует ответ для отображения"""
    sections = [
        ("КРАТКИЙ ОТВЕТ", "📝"),
        ("ПРИМЕНИМЫЕ ЗАКОНЫ", "⚖️"),
        ("ПОШАГОВАЯ ИНСТРУКЦИЯ", "📋"),
        ("НЕОБХОДИМЫЕ ДОКУМЕНТЫ", "📄"),
        ("КУДА ОБРАЩАТЬСЯ", "🏛️"),
        ("СРОКИ", "⏰"),
        ("ВОЗМОЖНЫЕ ТРЕБОВАНИЯ", "💰"),
    ]
    
    has_structure = any(section[0] in answer.upper() for section in sections)
    
    if not has_structure:
        return f"⚠️ **Ответ получен, но без стандартной структуры:**\n\n{answer}"
    
    return answer
