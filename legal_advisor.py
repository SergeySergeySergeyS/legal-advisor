def safe_decode(content):
    """Безопасно декодирует байты в строку"""
    if isinstance(content, bytes):
        return content.decode('utf-8', errors='ignore')
    return content


def ask_legal_advisor(question: str, category: str, chat_history: list = None, auth_key: str = "") -> str:
    """
    Задаёт вопрос юридическому консультанту на базе GigaChat
    """
    try:
        system_prompt = CATEGORY_PROMPTS.get(category, CATEGORY_PROMPTS["🛒 Защита прав потребителей"])
        
        messages = [Messages(role=MessagesRole.SYSTEM, content=system_prompt)]
        
        if chat_history:
            for msg in chat_history[-5:]:
                role = MessagesRole.USER if msg["role"] == "user" else MessagesRole.ASSISTANT
                messages.append(Messages(role=role, content=msg["content"]))
        
        messages.append(Messages(role=MessagesRole.USER, content=question))
        
        with GigaChat(
            credentials=auth_key,
            scope="GIGACHAT_API_PERS",
            verify_ssl_certs=False
        ) as giga:
            response = giga.chat(Chat(messages=messages))
            answer = safe_decode(response.choices[0].message.content)
        
        return answer
        
    except Exception as e:
        return f"❌ Произошла ошибка при получении ответа: {str(e)}\n\nПожалуйста, проверьте ключ API и попробуйте снова."


def format_answer_for_display(answer: str) -> str:
    """Форматирует ответ ИИ для отображения в чате"""
    if not answer:
        return "⚠️ Пустой ответ от ИИ"
    
    if "📝" in answer and "⚖️" in answer:
        return answer
    
    return answer
